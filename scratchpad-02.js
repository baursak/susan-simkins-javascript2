var sign = prompt('what is your astrological sign');
switch (sign) {
  case 'taurus':
    alert('you said taurus');
    break;
  case 'virgo':
    alert('you said virgo');
    break;
  case 'leo':
    alert('you said leo');
    break;
  default:
    alert('you said something unexpected');
    break;
}
